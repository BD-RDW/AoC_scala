  def calculatedCheckSum(strArr: Array[String]): Int = {
    var cntMap: Map[Int,Int] = Map((2 -> 0),(3 -> 0))
    strArr.foreach{
      str => findCharactersThatAppear2or3Times(str).values.filter(x => x == 2 || x == 3).toArray.distinct
        .foreach(x => {
          cntMap = cntMap + (x -> (cntMap.get(x).get + 1))
        })
    }
    cntMap.get(2).get *cntMap.get(3).get
  }

  def findCharactersThatAppear2or3Times(str: String): Map[Char, Int] = {
    var charMap: Map[Int,Int] = Map((1,1))
    str.chars()
      .forEach(chr => {
        val cnt = if (charMap.contains(chr)) charMap.get(chr).get + 1 else 1
        charMap = charMap + (chr -> cnt)
      })
    charMap.keys.filter(k => charMap.get(k).get == 2 || charMap.get(k).get == 3 ).map(k => (k.toChar -> charMap.get(k).get)).toMap
  }

